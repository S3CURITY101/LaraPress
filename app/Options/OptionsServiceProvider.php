    /**
     * Register bindings in the container.
     *
     * @return void
     */
    public function register()
    {
        $this->app->bind(OptionsRepository::class, function ($app) {
            return app(DefaultOptionsRepository::class);
        });

        $this->app->bind('options', function(){
            return app(OptionsProvider::class);
        });

        $this->loadCustomPostTypes();
